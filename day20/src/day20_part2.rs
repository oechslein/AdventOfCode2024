use miette::Result;

//#[tracing::instrument]
pub fn process(input: &str) -> Result<String> {
    Ok(crate::solve::solve(input, 100, 20).to_string())
    //Ok(crate::solve::solve(input, 50, 20).to_string())
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_example() -> miette::Result<()> {
        let input = "###############
#...#...#.....#
#.#.#.#.#.###.#
#S#...#.#.#...#
#######.#.#.###
#######.#.#...#
#######.#.###.#
###..E#...#...#
###.#######.###
#...###...#...#
#.#####.#.###.#
#.#...#.#.#...#
#.#.#.#.#.#.###
#...#...#...###
###############";
        assert_eq!(
            32 + 31 + 29 + 39 + 25 + 23 + 20 + 19 + 12 + 14 + 12 + 22 + 4 + 3, // 285
            crate::solve::solve(input, 50, 20)
        );
        Ok(())
    }

    #[test]
    fn test_input() -> miette::Result<()> {
        let input = include_str!("../input2.txt");
        assert_eq!("1009299", process(&input.replace('\r', ""))?);
        Ok(())
    }
}
